<?php
// Example PHP code for mathematical concepts and problems

// Define variables
$base = 5;
$height = 3;

// Calculate area of rectangle
$area = $base * $height;
echo "The area of the rectangle is: " . $area . "\n";

// Compare base with height
if ($base > $height) {
    echo "Base is greater than height.\n";
} else {
    echo "Height is greater than base.\n";
}

// Calculate perimeter of square
$perimeter = 4 * $base;
echo "The perimeter of the square is: " . $perimeter . "\n";

// Calculate area of triangle
$area_triangle = 0.5 * ($base * $height);
echo "The area of the triangle is: " . $area_triangle . "\n";
?>
